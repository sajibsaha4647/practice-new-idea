package ArrayList;

public class ArraylistModel {
    String name;
    int age;

   public ArraylistModel(String name,int age){
            this.name = name ;
            this.age = age;
    }

   public String getName(){
    return  name;
   }

   public String setName(String name){
    return  name;
   }

   public int getAge(){
    return  age;
   }

   public int setAge(int age){
    return  age;
   }




}
